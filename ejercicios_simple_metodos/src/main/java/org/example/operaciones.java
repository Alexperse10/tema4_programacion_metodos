package org.example;

public class operaciones {
    public static int sumar(int a, int b) {
        return a + b;
    }
    public static int cubo (int n) {
        return n*n*n;
    }
    public static int doble (int n){
        return n*2;
    }
    // todo los metodos son static para poder llamarlos desde main sin objetos
}
